#include "libft.h"

int		ft_atoi(char *str)
{
	int		i;
	int		a;
	long	b;

	i = 0;
	a = 0;
	b = 0;
	while (str[i] == '\t' || str[i] == '\n' || str[i] == '\r' ||
			str[i] == '\v' || str[i] == '\f' || str[i] == ' ' || str[i] == '+')
		i++;
	if (str[i] == '-')
	{
		a = 1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		b *= 10;
		b += str[i] - '0';
		i++;
	}
	if (a == 1)
		return (-b);
	return (b);
}
