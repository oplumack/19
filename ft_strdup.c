#include "libft.h"

char		*ft_strdup(const char *s)
{
	char	*copy;
	int		i;
	int		size;

	size = 0;
	while (s[size])
		++size;
	if (!(copy = malloc(sizeof(char) * (size + 1))))
		return (NULL);
	i = 0;
	while (s[i])
	{
		copy[i] = s[i];
		i++;
	}
	copy[i] = '\0';
	return (copy);
}
