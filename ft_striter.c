#include "libft.h"

void	ft_striter(char *s, char (*f)(char *))
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	if (s != NULL && f != NULL)
	{
		i = ft_strlen(s);
		while (j < i)
		{
			(*f)(s);
			s++;
			j++;
		}
	}
}
