#include "libft.h"

char	*ft_strstr(const char *str, const char *to_find)
{
	int		i;
	int		z;
	int		to_find_size;

	i = 0;
	z = 0;
	to_find_size = 0;
	while (to_find[to_find_size])
		to_find_size++;
	if (to_find_size == 0)
		return ((char *)str);
	while (str[i])
	{
		while (to_find[z] == str[i + z])
		{
			if (z == to_find_size - 1)
				return ((char *)(str + i));
			z++;
		}
		z = 0;
		i++;
	}
	return (0);
}
